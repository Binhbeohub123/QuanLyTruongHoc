@Override
public void login() {
    String username = txtMaso.getText().trim();
    String password = new String(txtPassword.getPassword());

    if (username.isEmpty()) {
        XDialog.alert("Vui l√≤ng nh·∫≠p t√™n ƒëƒÉng nh·∫≠p!");
        txtMaso.requestFocus();
        return;
    }

    if (password.isEmpty()) {
        XDialog.alert("Vui l√≤ng nh·∫≠p m·∫≠t kh·∫©u!");
        txtPassword.requestFocus();
        return;
    }

    try {
        UsersDAOImpl dao = new UsersDAOImpl();
        User user = dao.findById(username);

        if (user == null) {
            XDialog.alert("T√™n ƒëƒÉng nh·∫≠p kh√¥ng t·ªìn t·∫°i!");
            txtMaso.requestFocus();
        } else if (!password.equals(user.getMatKhau())) {
            XDialog.alert("Sai m·∫≠t kh·∫©u ƒëƒÉng nh·∫≠p!");
            txtPassword.requestFocus();
        } else if (!user.isActive()) {
            XDialog.alert("T√†i kho·∫£n c·ªßa b·∫°n ƒëang b·ªã kho√°!");
        } else {
            XAuth.user = user; // L∆∞u th√¥ng tin user
            
            this.dispose();
            
            // ‚úÖ TH√äM D√íNG N√ÄY:
            openMainForm(user);
        }
    } catch (Exception e) {
        XDialog.alert("L·ªói ƒëƒÉng nh·∫≠p: " + e.getMessage());
        e.printStackTrace();
    }
}
    
